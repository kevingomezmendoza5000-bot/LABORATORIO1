    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
   
        try {
            // TODO add your handling code here:
            //sirve para guardar y modificar
            reservas mireserva = new reservas();
            mireserva.setID_Reserva(Integer.parseInt(TXTRESERVA.getText()));
            mireserva.setID_Vuelo(Integer.parseInt(TXTVUELO.getText()));
            mireserva.setID_Pasajero(Integer.parseInt(TXTPASAJERO.getText()));
            mireserva.setPrecio(Integer.parseInt(TXTPRECIO.getText()));
            mireserva.setEstado(cmbEstado.getSelectedIndex());
            mireserva.setFecha_Reserva(jDateChooser1.getDate());
            
            int indice = 0;
            //actualizar
            
            reservas Consultada = Conexion.obtenerReserva(TXTRESERVA.getText());
            if (Consultada != null){
                indice = Consultada.getID_Reserva();
            }
            else {
                indice = 0;
            }
            
            //actualizarâ†‘
            
            //indice = buscarIndice();
            //Agregar a la lista correspondiente
            if (indice == 0)
            {
                try {
                    //servicio.agregarEspecie(miEspecie);
                    if(Conexion.insertReserva(mireserva)!=0)
                        JOptionPane.showMessageDialog(null, "ESPECIE GUARDADA");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex.toString());
                }
            }else
            {
                //Actualizar
                //servicio.modificarEspecie(indice, miEspecie);
                try {
                    //servicio.agregarEspecie(miEspecie);
                    if(Conexion.updateReserva(mireserva)!=0)
                        JOptionPane.showMessageDialog(null, "ESPECIE MODIFICADA");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex.toString());
                }
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(FRMRESERVAS.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

